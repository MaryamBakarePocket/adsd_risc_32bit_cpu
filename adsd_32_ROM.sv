module adsd_32_ROM(
    input [31:0] addr,
    input OE,
    output [31:0] data_out
);
reg [31:0] rom[0:15];
wire [3:0] index = addr[5:2]; // quad-byte addressing

initial begin
    rom[0] = 32'b00100110_00000000_00000000_00000001; // ADDI 
    rom[1] = 32'b00010000_10001011_10001111_10001011; // ADD 
	 rom[2] = 32'b00010001_00000000_00000000_00000001; // SUB 
    rom[3] = 32'b00100110_11111111_11111111_11101101; // OR 
    rom[4] = 32'b00010011_00101010_10011000_10110010; // AND
//    rom[5] = 32'b00000000_11101000_11111000_00011111; // SHL
	 rom[5] = 32'b00100110_00000000_00000000_00000001; // ADDI 
    rom[6] = 32'b00100101_00000000_11000011_11000111; // NOT 
    rom[7] = 32'b00110001_11000110_11101100_11001100; // BEQ
	 rom[8] = 32'b00100110_00000000_00000000_00000001; // ADDI 
    rom[9] = 32'b00100110_11111111_11111111_11111111; // ADDI 
    rom[10] = 32'b01010001_00000000_00000000_00000000; // JMP 0
    rom[11] = 32'b00000000_00000000_00000000_00000000; // NOP
end

assign data_out = OE ? rom[index] : 32'd0;
endmodule